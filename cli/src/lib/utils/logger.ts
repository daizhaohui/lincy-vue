export  function log(...args:any[]):void{
  // tslint:disable-next-line:no-console
  console.log(...args);
}


export  function error(err:any):void{
  // tslint:disable-next-line:no-console
  console.error(err);
}


export  function warn(...args:any[]):void{
  // tslint:disable-next-line:no-console
  console.warn(...args);
}

export  function info(...args:any[]):void{
  // tslint:disable-next-line:no-console
  console.info(...args);
}

